export const LESSON_PLAN_PROMPT = (topic, options = {}) => {
    return `Создай подробный план урока по теме "${topic}" со следующими параметрами:
- Длительность урока: ${options.duration || '45'} минут
- Тип урока: ${options.type || 'теория'}
- Формат проведения: ${options.format || 'очный'}
- Возраст учеников: ${options.age || '7-9'} лет

План должен включать:
1. Цели и задачи урока
2. Необходимые материалы и оборудование
3. Этапы урока с указанием времени:
   - Организационный момент (2-3 минуты)
   - Проверка домашнего задания (если применимо)
   - Актуализация знаний
   - Изучение нового материала
   - Закрепление
   - Рефлексия
   - Домашнее задание
4. Методические рекомендации для каждого этапа
5. Возможные проблемные моменты и способы их решения
6. Дифференцированные задания для учеников разного уровня подготовки

Учитывай следующие особенности:
- Для теоретического урока: больше внимания объяснению материала и его визуализации
- Для практического урока: больше времени на самостоятельную работу и практические задания
- Для комбинированного урока: сбалансированное сочетание теории и практики
- Для очного формата: использование классной доски и физических материалов
- Для онлайн формата: использование цифровых инструментов и онлайн-ресурсов
- Для смешанного формата: комбинация очных и онлайн активностей`;
};

export const HOMEWORK_CHECK_PROMPT = (homework, options = {}) => {
    const level = options.level || 'базовый';
    const checkType = options.checkType || 'комплексный';
    const gradeType = options.gradeType || 'балльное';
    const subject = options.subject || 'общий';

    let gradeScale;
    switch (gradeType) {
        case 'балльное':
            gradeScale = '5-балльная шкала (1-5)';
            break;
        case 'процентное':
            gradeScale = 'процентная шкала (0-100%)';
            break;
        case 'уровневое':
            gradeScale = 'уровневая шкала (A, B, C, D, F)';
            break;
        default:
            gradeScale = '5-балльная шкала (1-5)';
    }

    let criteriaPrompt = '';
    if (checkType === 'комплексный') {
        criteriaPrompt = `
1. Содержание:
   - Полнота раскрытия темы
   - Глубина анализа
   - Соответствие заданию
   - Логика изложения
2. Структура:
   - Организация текста
   - Связность изложения
   - Аргументация
3. Грамотность:
   - Орфография
   - Пунктуация
   - Грамматика
   - Стилистика`;
    } else if (checkType === 'орфография') {
        criteriaPrompt = `
1. Орфографические ошибки:
   - Правописание корней
   - Правописание приставок
   - Правописание суффиксов
   - Правописание окончаний
   - Слитное/раздельное написание
   - Употребление прописных букв`;
    } else if (checkType === 'пунктуация') {
        criteriaPrompt = `
1. Пунктуационные ошибки:
   - Знаки препинания в простом предложении
   - Знаки препинания в сложном предложении
   - Знаки препинания при прямой речи
   - Знаки препинания при обособленных членах
   - Знаки препинания при вводных конструкциях`;
    }

    return `Проверь домашнее задание со следующими параметрами:
Уровень подготовки: ${level}
Тип проверки: ${checkType}
Система оценивания: ${gradeScale}
Предмет: ${subject}

Домашнее задание:
${homework}

Критерии оценки:${criteriaPrompt}

Пожалуйста, предоставь анализ по следующим пунктам:
1. Детальный разбор по указанным критериям
2. Сильные стороны работы
3. Области для улучшения
4. Оценка с подробным обоснованием (используя выбранную шкалу оценивания)
5. Конкретные рекомендации по улучшению с учетом уровня подготовки
6. Дополнительные материалы для изучения (если требуется)

Особые указания:
- Учитывай указанный уровень подготовки при оценке
- Давай развернутые комментарии по каждому критерию
- Используй примеры из текста для иллюстрации замечаний
- Формулируй рекомендации с учетом уровня подготовки
- При выставлении оценки учитывай все аспекты работы`;
}; 