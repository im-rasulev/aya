function getStyleGuidelines(style) {
    const guidelines = {
        'академический': 'Используй формальный язык, четкую структуру, научную терминологию, подкрепляй информацию исследованиями и фактами',
        'креативный': 'Добавь творческие элементы, используй яркие примеры, метафоры, интерактивные элементы',
        'деловой': 'Сфокусируйся на конкретных результатах, используй бизнес-терминологию, добавь графики и диаграммы',
        'casual': 'Используй простой разговорный язык, добавь юмор, истории из жизни и интересные факты'
    };
    return guidelines[style] || guidelines['академический'];
}

